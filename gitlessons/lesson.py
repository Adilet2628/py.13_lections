# Git - расспределенная система контроля версий
# это система для отслеживания и ведения истории изминения файлов, в вашем проекте. При помощи растпределенности git еще и оргпнизовывается командная работа.


# 
# Репозиторий - это хранилище всего кода и истории его изминений


# Github - веб сайт для размещения git-репозиториев  в совместной разработке проектов

# Команды Git:


# 1 git init-> она создает локальный гит репозиторий у вас на пк,  в той директории в которой была прописана команда.

# 2 git status --> команда для проверки статуса наших файлов 

# 3 git add -> команда для добавления файлов в систему отслеживания гита, после этой команды изминения начинают фиксироваться  


# 4 git commit -m 'comment'-> команда для сохранения версии в локальном репозитории, вместе 
# с коментарием к изменениям 

# 5 git remote add origin <urk > -> это команда для того чтобы установить связь локального репозитория с удаленным в гитхабе 

# 6 git push -> команда для отправки ваших изминений в локальном репо в удаленный репо 

#      git push origin main











































































































































